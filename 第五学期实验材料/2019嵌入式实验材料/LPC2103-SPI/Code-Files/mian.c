
#include ".\SPI_HC595\SPI_HC595.h"	 //引用头文件


//数码管显示0~9
uint8  const  SegTab[10] = {0xC0,0XF9,0XA4,0XB0,0X99,0X92,0X82,0XF8,0X80,0X98};

/********************************************************************************
**函数名称：void myDelay (INT32U ulTime)
**函数功能：延时
**入口参数：ulTime
**出口参数：无
********************************************************************************/
void myDelay (INT32U ulTime)
{
    INT32U i;

    i = 0;
    while (ulTime--) {
        for (i = 0; i < 5000; i++);
    }
}			
/********************************************************************************
**函数名称：int main(void)
**函数功能：主函数，控制数码管循环显示0~9
**入口参数：无
**出口参数：无
********************************************************************************/			
			
int main(void)
{
    uint8 i;
	HC595_Init();
	while(1)
	{
		for(i=0;i<10;i++)
		{
			HC595_SendData(SegTab[i]);
			myDelay(50);
		}
	}
//	return 0;
}

